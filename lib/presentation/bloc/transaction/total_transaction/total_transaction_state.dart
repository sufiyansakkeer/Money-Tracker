part of 'total_transaction_cubit.dart';

sealed class TotalTransactionState extends Equatable {
  const TotalTransactionState();
}

final class TotalTransactionInitial extends TotalTransactionState {
  @override
  List<Object> get props => [];
}

final class TotalTransactionSuccess extends TotalTransactionState {
  final double totalIncome;
  final double totalExpense;

  const TotalTransactionSuccess({
    required this.totalIncome,
    required this.totalExpense,
  });

  @override
  List<Object> get props => [
        totalExpense,
        totalIncome,
      ];
}
